Cypress.Commands.add('getContentDocument', (waitForSelector) => {
  return cy.get('#timerIcon')
    .its('0.contentDocument.documentElement')
    .should(body => {
      expect(Cypress.$(body).has(waitForSelector).length).gt(0)
    })
    .then(cy.wrap)
})
